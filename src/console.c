static uint8_t vga_entry_color(uint8_t foreground, uint8_t background) {
    return foreground | (background << 4);
}

static void console_apply_colors(void) {
    uint8_t fg = terminal_color & 0x0F;
    uint8_t bg = (terminal_color >> 4) & 0x0F;
    fb_fg = fb_palette[fg];
    fb_bg = fb_palette[bg];
}

static uint16_t vga_entry(unsigned char uc, uint8_t color) {
    return (uint16_t) uc | ((uint16_t) color << 8);
}

static void console_update_cursor(void) {
    if (console_use_fb) {
        return;
    }

    uint16_t pos = (uint16_t) (terminal_row * VGA_WIDTH + terminal_column);
    outb(0x3D4, 0x0F);
    outb(0x3D5, (uint8_t) (pos & 0xFF));
    outb(0x3D4, 0x0E);
    outb(0x3D5, (uint8_t) ((pos >> 8) & 0xFF));
}

static void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= fb_info.width || y >= fb_info.height) {
        return;
    }

    uint8_t *base = (uint8_t *) (uintptr_t) fb_info.address;
    uint32_t offset = y * fb_info.pitch + x * (fb_info.bpp / 8U);

    if (fb_info.bpp == 32) {
        uint32_t *ptr = (uint32_t *) (base + offset);
        *ptr = color;
    } else if (fb_info.bpp == 24) {
        base[offset] = (uint8_t) (color & 0xFF);
        base[offset + 1] = (uint8_t) ((color >> 8) & 0xFF);
        base[offset + 2] = (uint8_t) ((color >> 16) & 0xFF);
    }
}

static void fb_draw_char(uint32_t row, uint32_t col, char ch) {
    uint32_t x0 = col * FB_CHAR_WIDTH;
    uint32_t y0 = row * FB_CHAR_HEIGHT;
    uint8_t index = (uint8_t) ch;

    if (index >= 128) {
        index = (uint8_t) '?';
    }

    for (uint32_t y = 0; y < 8; ++y) {
        uint8_t bits = font8x8[index][y];
        for (uint32_t x = 0; x < 8; ++x) {
            uint32_t color = (bits & (1U << (7 - x))) ? fb_fg : fb_bg;
            fb_put_pixel(x0 + x, y0 + y * 2, color);
            fb_put_pixel(x0 + x, y0 + y * 2 + 1, color);
        }
    }
}

static void fb_clear(void) {
    uint8_t *base = (uint8_t *) (uintptr_t) fb_info.address;
    uint32_t bytes_per_pixel = fb_info.bpp / 8U;
    uint32_t row_bytes = fb_info.pitch;

    for (uint32_t y = 0; y < fb_info.height; ++y) {
        uint8_t *row = base + y * row_bytes;
        for (uint32_t x = 0; x < fb_info.width; ++x) {
            uint32_t offset = x * bytes_per_pixel;
            if (fb_info.bpp == 32) {
                *(uint32_t *) (row + offset) = fb_bg;
            } else if (fb_info.bpp == 24) {
                row[offset] = (uint8_t) (fb_bg & 0xFF);
                row[offset + 1] = (uint8_t) ((fb_bg >> 8) & 0xFF);
                row[offset + 2] = (uint8_t) ((fb_bg >> 16) & 0xFF);
            }
        }
    }
}

static void fb_scroll(void) {
    uint8_t *base = (uint8_t *) (uintptr_t) fb_info.address;
    uint32_t row_bytes = fb_info.pitch;
    uint32_t scroll_px = FB_CHAR_HEIGHT;

    if (fb_info.height <= scroll_px) {
        return;
    }

    for (uint32_t y = 0; y < fb_info.height - scroll_px; ++y) {
        uint8_t *dst = base + y * row_bytes;
        uint8_t *src = base + (y + scroll_px) * row_bytes;
        for (uint32_t i = 0; i < row_bytes; ++i) {
            dst[i] = src[i];
        }
    }

    for (uint32_t y = fb_info.height - scroll_px; y < fb_info.height; ++y) {
        uint8_t *row = base + y * row_bytes;
        for (uint32_t i = 0; i < row_bytes; ++i) {
            row[i] = 0;
        }
    }
}

void console_init(void) {
    console_use_fb = 0;
    terminal_color = vga_entry_color(0x0F, 0x01);
    console_clear();
}

void console_init_framebuffer(const framebuffer_info_t *info) {
    if (info == NULL || info->address == 0 || info->width == 0 || info->height == 0) {
        console_init();
        return;
    }

    if (info->bpp != 24 && info->bpp != 32) {
        console_init();
        return;
    }

    fb_info = *info;
    console_use_fb = 1;

    terminal_color = vga_entry_color(0x0F, 0x00);
    console_apply_colors();

    fb_cols = fb_info.width / FB_CHAR_WIDTH;
    fb_rows = fb_info.height / FB_CHAR_HEIGHT;
    if (fb_cols == 0 || fb_rows == 0) {
        console_init();
        return;
    }

    terminal_row = 0;
    terminal_column = 0;
    fb_clear();
}

void console_set_color(uint8_t color) {
    terminal_color = color;
    if (console_use_fb) {
        console_apply_colors();
    }
}

void console_clear(void) {
    if (console_use_fb) {
        fb_clear();
        terminal_row = 0;
        terminal_column = 0;
        return;
    }

    for (size_t y = 0; y < VGA_HEIGHT; ++y) {
        for (size_t x = 0; x < VGA_WIDTH; ++x) {
            terminal_buffer[y * VGA_WIDTH + x] = vga_entry(' ', terminal_color);
        }
    }

    terminal_row = 0;
    terminal_column = 0;
    console_update_cursor();
}

static void vga_putc(char c) {
    if (c == '\n') {
        terminal_column = 0;
        ++terminal_row;
        if (terminal_row >= VGA_HEIGHT) {
            for (size_t y = 1; y < VGA_HEIGHT; ++y) {
                for (size_t x = 0; x < VGA_WIDTH; ++x) {
                    terminal_buffer[(y - 1) * VGA_WIDTH + x] = terminal_buffer[y * VGA_WIDTH + x];
                }
            }
            for (size_t x = 0; x < VGA_WIDTH; ++x) {
                terminal_buffer[(VGA_HEIGHT - 1) * VGA_WIDTH + x] = vga_entry(' ', terminal_color);
            }
            terminal_row = VGA_HEIGHT - 1;
        }
        console_update_cursor();
        return;
    }

    if (c == '\r') {
        terminal_column = 0;
        console_update_cursor();
        return;
    }

    if (c == '\b') {
        if (terminal_column > 0) {
            --terminal_column;
            terminal_buffer[terminal_row * VGA_WIDTH + terminal_column] = vga_entry(' ', terminal_color);
        }
        console_update_cursor();
        return;
    }

    terminal_buffer[terminal_row * VGA_WIDTH + terminal_column] = vga_entry((unsigned char) c, terminal_color);
    ++terminal_column;

    if (terminal_column == VGA_WIDTH) {
        terminal_column = 0;
        ++terminal_row;
        if (terminal_row >= VGA_HEIGHT) {
            for (size_t y = 1; y < VGA_HEIGHT; ++y) {
                for (size_t x = 0; x < VGA_WIDTH; ++x) {
                    terminal_buffer[(y - 1) * VGA_WIDTH + x] = terminal_buffer[y * VGA_WIDTH + x];
                }
            }
            for (size_t x = 0; x < VGA_WIDTH; ++x) {
                terminal_buffer[(VGA_HEIGHT - 1) * VGA_WIDTH + x] = vga_entry(' ', terminal_color);
            }
            terminal_row = VGA_HEIGHT - 1;
        }
    }

    console_update_cursor();
}

static void fb_putc(char c) {
    if (c == '\n') {
        terminal_column = 0;
        ++terminal_row;
        if (terminal_row >= fb_rows) {
            fb_scroll();
            terminal_row = fb_rows - 1;
        }
        return;
    }

    if (c == '\r') {
        terminal_column = 0;
        return;
    }

    if (c == '\b') {
        if (terminal_column > 0) {
            --terminal_column;
            fb_draw_char((uint32_t) terminal_row, (uint32_t) terminal_column, ' ');
        }
        return;
    }

    if (terminal_row >= fb_rows || terminal_column >= fb_cols) {
        return;
    }

    fb_draw_char((uint32_t) terminal_row, (uint32_t) terminal_column, c);
    ++terminal_column;

    if (terminal_column >= fb_cols) {
        terminal_column = 0;
        ++terminal_row;
        if (terminal_row >= fb_rows) {
            fb_scroll();
            terminal_row = fb_rows - 1;
        }
    }
}

void console_putc(char c) {
    if (console_use_fb) {
        fb_putc(c);
    } else {
        vga_putc(c);
    }
}

void console_putc_at(size_t row, size_t column, char c) {
    if (console_use_fb) {
        if (row >= fb_rows || column >= fb_cols) {
            return;
        }
        fb_draw_char((uint32_t) row, (uint32_t) column, c);
        return;
    }

    if (row >= VGA_HEIGHT || column >= VGA_WIDTH) {
        return;
    }
    terminal_buffer[row * VGA_WIDTH + column] = vga_entry((unsigned char) c, terminal_color);
}

void console_write(const char *text) {
    for (size_t i = 0; text[i] != '\0'; ++i) {
        console_putc(text[i]);
    }
}

void console_writeln(const char *text) {
    console_write(text);
    console_putc('\n');
}

void console_write_dec(int value) {
    char buffer[12];
    size_t index = 0;
    unsigned int number;

    if (value < 0) {
        console_putc('-');
        number = (unsigned int) (-(value + 1)) + 1U;
    } else {
        number = (unsigned int) value;
    }

    if (number == 0U) {
        console_putc('0');
        return;
    }

    while (number > 0U) {
        buffer[index++] = (char) ('0' + (number % 10U));
        number /= 10U;
    }

    while (index > 0U) {
        --index;
        console_putc(buffer[index]);
    }
}

void console_write_hex(uint32_t value) {
    static const char digits[] = "0123456789ABCDEF";

    console_write("0x");
    for (int shift = 28; shift >= 0; shift -= 4) {
        console_putc(digits[(value >> shift) & 0xF]);
    }
}

void console_set_cursor(size_t row, size_t column) {
    terminal_row = row;
    terminal_column = column;
    if (!console_use_fb) {
        console_update_cursor();
    }
}
